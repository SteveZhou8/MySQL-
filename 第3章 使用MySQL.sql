1. USE DATABASE: 选择数据库
2. SHOW DATABASES：返回可用数据库的一个列表
3. SHOW TABLES：返回当前选择的数据库内可用表的列表
4. SHOW COLUMNS FROM TABLE：显示表列，可简写describe或desc
5. SHOW STATUS：显示广泛的服务器状态信息
6. SHOW CREATE DATABASE：显示创建特定数据库的Mysql语句
7. SHOW CREATE TABLE：显示创建特定表的Mysql语句
8. SHOW GRANTS：显示授权用户（所有用户或特定用户）的安全权限
9. SHOW ERRORS：显示服务器错误信息
10. SHOW WARNINGS：显示服务器警告信息
